//switch statement
void main() {
  int x = 3;
  switch (x) {
    case 1:
      print("mon");
    case 2:
      print("tue");
    case 3:
      print("wed");
    case 4:
      print("thur");
    case 5:
      print("fri");
    case 6:
      print("sat");
    case 7:
      print("sun");
    case 8:
      print("invalid input");
  }
}
